# 11. Матриця 5x4 заповнюється введенням з клавіатури (крім останніх елементів рядків). Програма повинна обчислювати
# суму введених елементів кожного рядка і записувати її в останній рядок. Наприкінці слід вивести отриману матрицю.
n = 5
m = 4
a = [[int(input('Введіть число:  ')) for i in range(n)] for j in range(m)]
c = [sum(a[i]) for i in range(n-1)]
a.append(c)
for i in range(n):
    print(' '.join(map(str, a[i])))
