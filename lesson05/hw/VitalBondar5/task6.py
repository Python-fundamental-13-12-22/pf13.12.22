# У другому списку зберегти індекси парних елементів першого списку. Наприклад, якщо дано список зі значеннями
# 8, 3, 15, 6, 4, 2, то другий треба заповнити значеннями 1, 4, 5, 6 або ( 0, 3, 4, 5 -
# якщо індексація починається з нуля), оскільки саме в цих позиціях першого масиву стоять парні числа.
import random
a = [0]*10
for i in range(10):
    a[i] = random.randint(-50, 50)
print(a)
c = []
for i in range(10):
    if a[i] % 2 == 0:
        c.append(a.index(a[i]))
print(c)
