# Заповнити список дійсними числами введенням з клавіатури. Порахувати суму і добуток елементів списку.
# Вивести на екран сам список, отримані суму і добуток його елементів.
n = int(input('Скільки дійсних чисел ви хочете ввести :  '))
b = []
for i in range(n):
    y = float(input('Введіть число:  '))
    b.append(y)
print('Введено список', b)
s = sum(b)
print('Сума елементів',s)
dob = 1
for i in range(n):
    dob = dob*b[i]
print('Добуток', dob)
