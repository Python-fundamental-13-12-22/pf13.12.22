""". Написати функцію `square`, яка приймає 1 аргумент - сторону квадрата, і повертає 3 значення
(за допомогою кортежу): периметр квадрата, площу квадрата і діагональ квадрата."""


def square(side):
    p = 4 * side
    s = side ** 2
    d = round((2 * s) ** 0.5, 2)
    result = (p, s, d)
    return result


print(square(8))
