# 11. Для чисел, що вводяться користувачем, визначити відсоток додатних та від’ємних чисел.
# При введенні числа 0 закінчити роботу.
n = int(input("Скільки чисел ви хочете ввести: "))
i = 1
dod = 0
vid = 0
while i <= n:
    k = float(input("Введіть дійсне число  :  "))
    i += 1
    if k > 0:
        dod = dod + 1
    elif k < 0:
        vid = vid + 1
    elif k == 0:
        break
print(dod*100/(dod+vid), '% додатних чисел')
print(vid*100/(dod+vid), "% від'ємних чисел")
