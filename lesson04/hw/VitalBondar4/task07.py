# Вводяться десять натуральних чисел більше 2. Порахувати, скільки серед них чисел,
# що кратні 5-ти. (не використовувати лісти)
i = 1
kr = 0
while i < 11:
    k = int(input("Введіть натуральне число більше 2 : "))
    i += 1
    if k % 5 == 0:
        kr = kr + 1
print('Кратних 5 введено', kr, 'чисел')
