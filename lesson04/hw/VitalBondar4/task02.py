# 2. Ввести три значення (рік, місяць, день) у відповідні змінні. Визначити
# чи введені дані відповідають коректному запису дати.
day = int(input("Веедіть день : "))
mon = int(input("Веедіть місяць : "))
yea = int(input("Веедіть рік : "))
if day in range(1, 31) and mon in range(1, 12) and yea in range(1900, 2022):
    print("Дата записана коректно")
else:
    print("Дата записана некоректно")